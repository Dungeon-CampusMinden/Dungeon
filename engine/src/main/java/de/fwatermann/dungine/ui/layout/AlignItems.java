package de.fwatermann.dungine.ui.layout;

/**
 * Enum representing the alignment of items on the cross axis (not the direction axis) of a line in
 * a container.
 */
public enum AlignItems {
  /** Items are packed at the start of the line. */
  FLEX_START,

  /** Items are packed at the end of the line. */
  FLEX_END,

  /** Items are centered in the line. */
  CENTER,

  /** Items are stretched to fill the line. */
  STRETCH,
}
