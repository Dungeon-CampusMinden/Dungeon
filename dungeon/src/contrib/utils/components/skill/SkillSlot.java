package contrib.utils.components.skill;

/**
 * Enumeration of different skill-slots.
 *
 * <p>Skill slots are designed to allow a skill to be assigned to each mouse button, which can then
 * be switched between them as desired.
 */
public enum SkillSlot {
  /** First slot for the left mouse button. */
  FIRST_SLOT,
  /** Second slot for the right mouse button. */
  SECOND_SLOT
}
